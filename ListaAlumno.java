
package registroescolar;

import java.util.ArrayList;


public class ListaAlumno {
    ArrayList nombre;
    ArrayList codigo;
    ArrayList alias;
    ArrayList correo;
    ArrayList telefono;
    ArrayList codigoCurso;
    ArrayList nombreCurso;
    ArrayList codigoGrado;
    ArrayList descripcion;

    public ListaAlumno(ArrayList nombre, ArrayList codigo, ArrayList alias, ArrayList correo, ArrayList telefono, ArrayList codigoCurso, ArrayList nombreCurso, ArrayList codigoGrado, ArrayList descripcion) {
        this.nombre = nombre;
        this.codigo = codigo;
        this.alias = alias;
        this.correo = correo;
        this.telefono = telefono;
        this.codigoCurso = codigoCurso;
        this.nombreCurso = nombreCurso;
        this.codigoGrado = codigoGrado;
        this.descripcion = descripcion;
    }
    
    
    
}
